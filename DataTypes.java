class DataTypeExample {

    public static void main(String[] args) {
        

        
        byte userAge = 25; 
        short temperature = -10; 
        int populationCount = 2000000; 
        long nationalDebt = 10000000000L; 
       
        float heightInMeters = 1.75f; 
        double preciseValue = 3.1415926535; 
        
        char grade = 'A';
         

       
        boolean isJavaFun = true; 


      
        String greetingMessage = "Hello, Java Developer!"; 

         System.out.println("User Age (byte): " + userAge);
        System.out.println("Temperature (short): " + temperature);
        System.out.println("Population (int): " + populationCount);
        System.out.println("National Debt (long): " + nationalDebt);
        System.out.println("Height (float): " + heightInMeters);
        System.out.println("Pi value (double): " + preciseValue);
        System.out.println("Grade (char): " + grade);
        System.out.println("Is Java fun? (boolean): " + isJavaFun);
        System.out.println("Greeting Message (String): " + greetingMessage);
        }
    }

       
        
       


       
       

        

